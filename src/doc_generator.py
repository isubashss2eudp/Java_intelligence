def _build_llm_context(data: _AnalysisData, framework: str) -> str:
    """Build a compact, comprehensive text summary for the LLM prompt context."""
    sp    = data.spring_patterns
    stats = data.stats
    lines: List[str] = [
        "=== CODEBASE ANALYSIS DATA ===",
        f"Framework     : {framework}",
        f"Base Package  : {data.base_package or 'unknown'}",
        f"Source Files  : {data.total_files}",
        f"Total Classes : {data.total_classes}",
        f"Detected      : {stats.get('detected_classes', 0)} / {stats.get('total_classes', 0)}"
        f" ({stats.get('detection_rate', 0)*100:.0f}% classified)",
        f"Entry Point   : {sp.get('entry_point_class', 'not found')}",
        "",
    ]

    # Layers with class names
    if data.layers:
        lines.append("ARCHITECTURAL LAYERS:")
        for layer in data.layers:
            class_names: List[str] = []
            for role_key in layer.get("roles", []):
                class_names += [c["class_name"] for c in data.roles.get(role_key, [])[:6]]
            pkgs = ", ".join(sorted(set(layer.get("packages", []))))
            lines.append(
                f"  {layer['name']} ({layer['class_count']} classes): "
                f"{', '.join(class_names[:5])}"
                + (" ..." if len(class_names) > 5 else "")
            )
            if pkgs:
                lines.append(f"    packages: {pkgs}")
        lines.append("")

    # Classes by role with annotations
    if data.roles:
        lines.append("CLASSES BY ROLE:")
        for role_key, classes in sorted(data.roles.items(), key=lambda x: -len(x[1])):
            if not classes or role_key == "unknown":
                continue
            names = ", ".join(c["class_name"] for c in classes[:5])
            if len(classes) > 5:
                names += f" (+{len(classes)-5} more)"
            anns = list({a for c in classes for a in c.get("annotations", [])})[:4]
            lines.append(f"  {role_key}: {names}")
            if anns:
                lines.append(f"    annotations: {', '.join('@'+a for a in anns)}")
        lines.append("")

    # Framework patterns
    for pat in sp.get("patterns_detected", []):
        lines.append(f"  Pattern: {pat}")
    for k, v in {
        "REST Controllers": sp.get("rest_controller_count", 0),
        "JPA Entities": sp.get("jpa_entity_count", 0),
        "Spring Data Repos": sp.get("spring_data_repo_count", 0),
    }.items():
        if v:
            lines.append(f"  {k}: {v}")
    lines.append("")

    # Modules
    if data.modules:
        lines.append("FEATURE MODULES:")
        for m in data.modules:
            lines.append(
                f"  {m['name']}: {m.get('class_count',0)} classes "
                f"[{', '.join(m.get('roles',[]))}]"
                + (" [full-stack]" if m.get("is_full_stack") else "")
            )
        lines.append("")

    # Dependency analysis
    if data.coupling_metrics:
        lines.append("DEPENDENCY ANALYSIS:")
        lines.append(f"  Circular deps: {len(data.circular_deps)}")
        for cd in data.circular_deps:
            cycle = cd.get("cycle", [])
            lines.append(f"    Cycle: {' -> '.join(cycle + [cycle[0]])}")
        lines.append(f"  Orphans ({len(data.orphan_classes)}): {', '.join(data.orphan_classes[:5])}")
        lines.append(f"  Layer violations: {len(data.layer_violations)}")
        for v in data.layer_violations[:5]:
            lines.append(f"    {v.get('source')} -> {v.get('target')}")
        lines.append("")

        lines.append("COUPLING METRICS (Ca=afferent/in Ce=efferent/out I=instability):")
        top = sorted(data.coupling_metrics,
                     key=lambda m: -(m.get("Ca",0)+m.get("Ce",0)))[:12]
        for m in top:
            if m.get("Ca", 0) + m.get("Ce", 0) == 0:
                continue
            flag = " [HIGHLY COUPLED]" if m.get("highly_coupled") else ""
            lines.append(
                f"  {m['class_name']:<35} Ca={m['Ca']:>2} Ce={m['Ce']:>2} "
                f"I={m['instability']:.2f}{flag}"
            )
        lines.append("")

    # C4 excerpt
    if data.c4_text:
        lines.append("C4 MODEL (summary):")
        for line in data.c4_text.splitlines()[:25]:
            lines.append("  " + line)
        lines.append("")

    return "\n".join(lines)


