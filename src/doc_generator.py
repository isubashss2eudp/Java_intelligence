"""
Documentation generator.

Produces a personalised .docx report in C:/temp with content that is
either LLM-generated (when an LLM instance is supplied) or falls back
to structured templates.

Framework detection
-------------------
The generator inspects the actual annotations and patterns extracted from
the codebase to decide whether it is dealing with:
  - Spring Boot  (has @SpringBootApplication / @RestController / Spring Data)
  - Spring       (uses some Spring annotations but not the full Boot stack)
  - Java         (plain Java application with no Spring)

Nothing is hard-coded as "Spring Boot".

LLM narrative
-------------
When an LLM is supplied, each major analytical section is generated by
the LLM using the actual analysis data as context.  Static python-docx
tables with concrete metrics are always included regardless.

Content depth by knowledge level
---------------------------------
  beginner     -- plain-language overview, components, getting-started, glossary
  intermediate -- architecture layers, patterns, component map, navigation guide
  advanced     -- dependency analysis, coupling interpretation, request flow, debt
  expert       -- full coupling metrics, C4 model, recommendations
"""

