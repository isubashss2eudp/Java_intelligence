def _build_title_page(doc: Any, profile: UserProfile, data: _AnalysisData,
                      framework: str) -> None:
    doc.add_paragraph()
    doc.add_paragraph()

    sp      = data.spring_patterns
    app_name = (
        sp.get("entry_point_class", "").replace("Application", " Application").strip()
        or (data.base_package.split(".")[-1].replace("-", " ").title() + " Application"
            if data.base_package else "Java Application")
    )

    tp = doc.add_paragraph()
    tp.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = tp.add_run(app_name)
    run.bold = True
    run.font.size = Pt(28)
    _set_run_colour(run, _CLR_DARK_BLUE)

    sp_para = doc.add_paragraph()
    sp_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = sp_para.add_run(f"{framework} — Analysis Report")
    run.font.size = Pt(16)
    _set_run_colour(run, _CLR_MID_BLUE)

    doc.add_paragraph()
    level_labels = {
        "beginner":     "Beginner Guide",
        "intermediate": "Technical Overview",
        "advanced":     "Technical Deep Dive",
        "expert":       "Expert Architectural Assessment",
    }
    lp = doc.add_paragraph()
    lp.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = lp.add_run(level_labels.get(profile.knowledge_level, "Analysis Report"))
    run.font.size = Pt(14)
    run.italic    = True
    _set_run_colour(run, _CLR_TEAL)

    doc.add_paragraph()
    doc.add_paragraph()
    for label, value in [
        ("Prepared for", profile.name),
        ("Role",         profile.role),
        ("Purpose",      profile.purpose),
        ("Framework",    framework),
        ("Date",         datetime.now().strftime("%Y-%m-%d %H:%M")),
        ("Generated by", "RetroDecrypt Platform"),
    ]:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r1 = p.add_run(f"{label}: ")
        r1.bold = True
        r1.font.size = Pt(11)
        r2 = p.add_run(value)
        r2.font.size = Pt(11)

    _page_break(doc)


