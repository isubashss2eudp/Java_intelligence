# ---------------------------------------------------------------------------
# Framework detection  (never assumes Spring Boot)
# ---------------------------------------------------------------------------

_SPRING_ANNOTATIONS = frozenset({
    "SpringBootApplication", "EnableAutoConfiguration",
    "RestController", "RequestMapping", "GetMapping", "PostMapping",
    "PutMapping", "DeleteMapping", "PatchMapping",
    "Service", "Repository", "Component", "Configuration",
    "Autowired", "Inject", "Bean", "Transactional",
    "Entity", "Table", "MappedSuperclass",
    "EnableJpaRepositories", "EnableWebSecurity",
    "Scheduled", "Async", "EventListener",
})

_SPRING_IMPORT_PREFIXES = (
    "org.springframework.",
    "javax.persistence.",
    "jakarta.persistence.",
)


def detect_framework(data: _AnalysisData) -> str:
    """
    Determine the framework from detected annotations, patterns, and imports.
    Returns one of: 'Spring Boot', 'Spring', 'Java'
    """
    sp = data.spring_patterns

    # Definitive: @SpringBootApplication found
    if sp.get("has_spring_boot_main"):
        return "Spring Boot"

    # Two or more distinct Spring Boot features present
    boot_features = [
        sp.get("rest_controller_count", 0) > 0,
        sp.get("spring_data_repo_count", 0) > 0,
        sp.get("jpa_entity_count", 0) > 0,
        sp.get("has_security"),
        sp.get("has_scheduling"),
        sp.get("has_async"),
    ]
    if sum(bool(f) for f in boot_features) >= 2:
        return "Spring Boot"

    # Count Spring annotations and check imports across all files
    all_annotations: set = set()
    spring_import_found = False
    for fm in data.metadata:
        all_annotations.update(fm.get("annotations", []))
        for imp in fm.get("imports", []):
            if any(imp.startswith(p) for p in _SPRING_IMPORT_PREFIXES):
                spring_import_found = True

    spring_ann_count = len(all_annotations & _SPRING_ANNOTATIONS)
    core_boot = {"RestController", "SpringBootApplication", "EnableAutoConfiguration"}

    if spring_ann_count >= 3 or (spring_ann_count >= 1 and spring_import_found):
        return "Spring Boot" if (all_annotations & core_boot) else "Spring"
    if spring_import_found or spring_ann_count >= 1:
        return "Spring"

    return "Java"


