def generate_documentation(profile: UserProfile, llm: Any = None) -> Path:
    """
    Generate a tailored .docx analysis report.

    Parameters
    ----------
    profile : UserProfile
        Collected user profile with a computed knowledge_level.
    llm : optional
        A loaded LangChain LLM instance.  When supplied, each analytical
        section is generated by the LLM using the actual codebase analysis
        data as grounding context.  When None, structured template content
        is written instead.

    Returns
    -------
    Path
        Absolute path to the generated .docx file saved in C:/temp.

    Raises
    ------
    ImportError
        If python-docx is not installed.
    """
    if not _DOCX_AVAILABLE:
        raise ImportError("python-docx is not installed.  Run: pip install python-docx")

    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    data      = _AnalysisData()
    framework = detect_framework(data)
    doc       = Document()

    if llm:
        print(f"  Detected framework : {framework}")
        print("  LLM section generation (one LLM call per section)…")

    _build_doc(doc, profile, data, framework, llm)

    safe_name   = "".join(c if c.isalnum() or c in "_- " else "_" for c in profile.name).replace(" ", "_")
    timestamp   = datetime.now().strftime("%Y%m%d_%H%M%S")
    level_label = profile.knowledge_level.title()
    filename    = f"JavaIntelligence_{safe_name}_{level_label}_{timestamp}.docx"
    out_path    = OUTPUT_DIR / filename

    doc.save(str(out_path))
    return out_path
