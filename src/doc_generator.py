def _call_llm_section(
    llm: Any,
    profile: UserProfile,
    data: _AnalysisData,
    framework: str,
    section: str,
    context: str,
    label: str = "",
) -> str:
    """
    Call the LLM to generate narrative text for one document section.
    Returns generated text, or empty string on failure.
    """
    template = _SECTION_PROMPTS.get(section, {}).get(profile.knowledge_level, "")
    if not template:
        return ""

    instruction = template.format(
        framework=framework,
        layer_count=len(data.layers),
        cycle_count=len(data.circular_deps),
    )

    system = (
        f"You are a senior Java architect writing technical documentation.\n"
        f"Audience: {profile.role} | Java expertise: {profile.java_expertise}.\n"
        f"Purpose: {profile.purpose}. Depth: {profile.depth}.\n"
        f"Rules:\n"
        f"  1. Always cite actual class names and metrics from the analysis data provided.\n"
        f"  2. Be specific and analytical — interpret, not just describe.\n"
        f"  3. Do not invent facts not present in the data.\n"
        f"  4. Write flowing prose only. No markdown headings or bullet points.\n"
        f"  5. Separate logical paragraphs with a single blank line."
    )

    try:
        from langchain_core.messages import SystemMessage, HumanMessage
        print(f"    [{label or section}] generating…", end="", flush=True)
        response = llm.invoke([
            SystemMessage(content=system),
            HumanMessage(content=f"{context}\n\nTask:\n{instruction}"),
        ])
        text = getattr(response, "content", "").strip()
        print(" done." if text else " (no content).")
        return text
    except Exception as exc:
        print(f" failed: {exc}")
        return ""


