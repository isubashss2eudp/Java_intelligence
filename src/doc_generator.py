def _sec_c4(doc: Any, data: _AnalysisData) -> None:
    _heading(doc, "9. C4 Architecture Model", level=1)
    if not data.c4_text:
        _para(doc, "C4 model not available. Run build_architecture.py first.", italic=True)
        return
    _para(doc, "The C4 model describes the system at System Context, Container, and Component levels.")
    doc.add_paragraph()
    for line in data.c4_text.splitlines():
        s = line.strip()
        if not s or s.startswith("==="):
            continue
        if "C4 Model --" in s:
            _heading(doc, s.replace("C4 Model -- ", ""), level=2, colour=_CLR_MID_BLUE)
        elif s.startswith("[") or ":" in s:
            _para(doc, s, bold=("System:" in s or "Container:" in s))
        else:
            _para(doc, s)


def _sec_debt(doc: Any, profile: UserProfile, data: _AnalysisData,
              framework: str, llm: Any, ctx: str) -> None:
    _heading(doc, "10. Technical Debt", level=1)
    review = _load_json(DATA_DIR / "code_review_report.json")
    if review:
        _para(doc, f"Quality score: {review.get('quality_score', 0):.1f}/100  "
                   f"(Grade: {review.get('grade', 'N/A')})", bold=True)
        doc.add_paragraph()
    text = _call_llm_section(llm, profile, data, framework, "debt", ctx, "Technical Debt") if llm else ""
    if text:
        _write_llm_text(doc, text)
    else:
        if data.circular_deps:
            _para(doc, f"⚠  {len(data.circular_deps)} circular dependency cycle(s).", colour=_CLR_ORANGE)
        if data.layer_violations:
            _para(doc, f"⚠  {len(data.layer_violations)} layer violation(s).", colour=_CLR_ORANGE)
    if review and review.get("category_summaries"):
        doc.add_paragraph()
        _heading(doc, "Findings by Category", level=2, colour=_CLR_MID_BLUE)
        _table(doc, ["Category", "Total", "Critical", "High", "Medium", "Low"], [
            [s["category"], str(s["total"]), str(s["critical"]),
             str(s["high"]), str(s["medium"]), str(s["low"])]
            for s in review["category_summaries"] if s["total"] > 0
        ])


