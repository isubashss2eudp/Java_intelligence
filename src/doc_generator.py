def _set_run_colour(run: Any, rgb: tuple) -> None:
    run.font.color.rgb = RGBColor(*rgb)


def _heading(doc: Any, text: str, level: int = 1,
             colour: tuple = _CLR_DARK_BLUE) -> None:
    h = doc.add_heading(text, level=level)
    h.alignment = WD_ALIGN_PARAGRAPH.LEFT
    for run in h.runs:
        _set_run_colour(run, colour)


def _para(doc: Any, text: str, bold: bool = False, italic: bool = False,
          size: int = 11, colour: Optional[tuple] = None) -> None:
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold   = bold
    run.italic = italic
    run.font.size = Pt(size)
    if colour:
        _set_run_colour(run, colour)


def _write_llm_text(doc: Any, text: str) -> None:
    """Write LLM-generated prose to the document, respecting paragraph breaks."""
    for block in text.split("\n\n"):
        block = block.strip()
        if block:
            _para(doc, block)


def _bullet(doc: Any, text: str, level: int = 0) -> None:
    style = "List Bullet" if level == 0 else "List Bullet 2"
    doc.add_paragraph(style=style).add_run(text)


def _table(doc: Any, headers: List[str], rows: List[List[str]],
           style: str = "Light List Accent 1") -> None:
    if not rows:
        return
    try:
        tbl = doc.add_table(rows=1, cols=len(headers))
        tbl.style = style
    except KeyError:
        tbl = doc.add_table(rows=1, cols=len(headers))
    hdr_row = tbl.rows[0]
    for i, h in enumerate(headers):
        cell = hdr_row.cells[i]
        cell.text = h
        for run in cell.paragraphs[0].runs:
            run.bold = True
    for data_row in rows:
        row = tbl.add_row()
        for i, val in enumerate(data_row):
            row.cells[i].text = str(val) if val is not None else ""
    doc.add_paragraph()


def _page_break(doc: Any) -> None:
    doc.add_page_break()


